
public class rec {
	
	double y,x,height,width;
	String id,style;
	public rec(double y, double x, double height, double width, String id,
			String style) {
		super();
		this.y = y;
		this.x = x;
		this.height = height;
		this.width = width;
		this.id = id;
		this.style = style;
	}
	
		public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	
	

}
